CREATE TABLE trips (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    driver_name TEXT,
    passenger_name TEXT,
    pick_up_address TEXT NOT NULL,
    drop_off_address TEXT NOT NULL,
    pick_up_lat TEXT,
    pick_up_lng TEXT,
    pick_up_zip_code TEXT,
    drop_off_lat TEXT,
    drop_off_lng TEXT,
    drop_off_zip_code TEXT,
    driver_viewing BOOLEAN DEFAULT FALSE NOT NULL,
    last_driver_viewed BIGINT,
    number_of_stops BIGINT DEFAULT 0 NOT NULL,
    stops TEXT [] DEFAULT '{}' NOT NULL, 
    distance TEXT,
    duration TEXT,
    price TEXT,
    scheduled BOOLEAN DEFAULT FALSE NOT NUll,
    scheduled_date_time TIMESTAMP,
    time_driver_accepted TIMESTAMP,
    time_passenger_got_in TIMESTAMP,
    trip_complete BOOLEAN DEFAULT FALSE NOT NULL,
    time_trip_completed TIMESTAMP,
    time_requested TIMESTAMP,
    request_confirmed BOOLEAN DEFAULT TRUE NOT NULL,
    driver_accepted BOOLEAN DEFAULT FALSE NOT NULL,
    driver_id BIGINT,
    passenger_id BIGINT REFERENCES passengers(id) NOT NULL,
    date_created TIMESTAMP
);
CREATE TABLE trips (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    driver_name TEXT,
    passenger_name TEXT,
    pick_up_address TEXT NOT NULL,
    drop_off_address TEXT NOT NULL,
    pick_up_lat TEXT,
    pick_up_lng TEXT,
    drop_off_lat TEXT,
    drop_off_lng TEXT,
    number_of_stops BIGINT DEFAULT 0 NOT NULL,
    stops TEXT [] DEFAULT '{}' NOT NULL, 
    distance DECIMAL DEFAULT 0.00 NOT NULL,
    duration INTEGER DEFAULT 0 NOT NULL,
    price DECIMAL DEFAULT 0.00 NOT NULL,
    scheduled BOOLEAN DEFAULT FALSE NOT NUll,
    scheduled_date_time TEXT,
    time_driver_accepted TIMESTAMP,
    time_passenger_got_in TIMESTAMP,
    complete BOOLEAN DEFAULT FALSE NOT NULL,
    time_trip_completed TIMESTAMP,
    time_requested TIMESTAMP,
    request_confirmed BOOLEAN NOT NULL,
    driver_accepted BOOLEAN,
    driver_id BIGINT NOT NULL,
    passenger_id BIGINT REFERENCES passengers(id) NOT NULL,
    date_created TIMESTAMP NOT NULL
);